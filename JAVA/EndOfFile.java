import java.io.*;
import java.util.*;
public class Solution {
    public static void main(String[] args) {
        int count=0;
        Scanner sc=new Scanner(System.in);
        for(int i=0;i<3;i++)
        {
            
            String s=sc.nextLine();
            System.out.println(++count+" "+s);
        }
    }
}
